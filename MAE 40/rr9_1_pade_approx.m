% n = 1
figure(1);
num = [-0.5 1];
f_n = 1 - roots(num) / 1 + roots(num);
denom = [0.5 1];
f_d = 1 - roots(denom) / 1 + roots(denom);
hold on;
title("1st Order Pade Approximation");
ylabel("F(s)");
xlabel("ds");
plot(roots(num), f_n, 'o');
plot(roots(denom), f_d, 'X');
hold off;
